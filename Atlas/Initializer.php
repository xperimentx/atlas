<?php
namespace Atlas;

use Atlas;

trait Initializer
{
    function Initialize()
    {
        Atlas::$root_path = substr (__DIR__, 0, -6 );

        //¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨ Autoloader
        if (Cfg::$cfg_use_autoloader)
        {
            include_once __DIR__.'/Autoloader.php';
            Autoloader::Register();
        }


        //¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨ Configuration files
        self::Load_file_if_exists(Cfg::$cfg_file            );
        self::Load_file_if_exists(Cfg::$cfg_file_autoloader );





        //¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨¨ Database
        if (Cfg::$mysql_user_name)
        {
            Atlas::$db = new Mysql();
            Atlas::$db->Connect(    Cfg::$mysql_server   ,
                                    Cfg::$mysql_user_name,
                                    Cfg::$mysql_password ,
                                    Cfg::$mysql_db_name  ,
                                    Cfg::$mysql_charset  ,
                                    Cfg::$mysql_port     ,
                                    Cfg::$mysql_socket   );
        }
    }
}

